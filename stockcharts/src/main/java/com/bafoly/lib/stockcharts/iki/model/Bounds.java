public class Bounds {

    public int left;

    public int top;

    public int right;

    public int bottom;

    @Override
    public String toString() {
        return "Bounds{" +
                "left=" + left +
                ", top=" + top +
                ", right=" + right +
                ", bottom=" + bottom +
                '}';
    }
}
